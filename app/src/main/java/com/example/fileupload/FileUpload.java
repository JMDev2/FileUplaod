package com.example.fileupload;

public class FileUpload {
    public String name;
    public String url;

    public FileUpload(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public FileUpload() {
    }

    public String getName() {
        return name;
    }



    public String getUrl() {
        return url;
    }


}
